#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*ptr;

	ptr = malloc(sizeof(t_list));
	if (ptr == (void *)0)
		return ((void *)0);
	else
	{
		ptr->content = content;
		ptr->next = (void *)0;
		return (ptr);
	}
}
