#include "minishell.h"

char	str_cmp_by_n(char *str_1, char *str_2, size_t n)
{
	if (n == 0)
		return (0);
	while (n-- && *str_1 && (*str_1 == *str_2))
	{
		if (n == 0 || (*str_1 == '\0' && *str_2 == '\0'))
			return (0);
		str_1++;
		str_2++;
	}
	return (*str_1 - *str_2);
}
